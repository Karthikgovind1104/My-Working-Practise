package com.string;

public class TypeCasting {
	public static void main(String[] args) {
           
		// Narrowing type casting
		 int a = 100;                  
		 byte b = (byte) a;
		 System.out.println(b);
		 
		 // Widening type casting
		  short c = 20;
		  int d = c ;
		  System.out.println(d);
	}
}
