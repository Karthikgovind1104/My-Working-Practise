package com.cntrl;

public class ForLoop_P6 {
	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j <= 2; j++) {
				System.out.print(j);
			}
			for (int j = 1; j <= 1; j++) {
				System.out.print(j);
			}
			System.out.println();
		}

	}

}
